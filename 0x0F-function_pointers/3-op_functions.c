#include "3-calc.h"

/**
 * op_add - add two values
 * @a: first value
 * @b: second value
 *
 * Return: sum of two values
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two values
 * @a: first value
 * @b: second value
 *
 * Return: difference of two values
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two values
 * @a: first value
 * @b: second value
 *
 * Return: product of two values
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: first value
 * @b: second value
 *
 * Return: quotient of two values
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds remainder
 * @a: first number
 * @b: second number
 *
 * Return: modulus of two values
 */

int op_mod(int a, int b)
{
	return (a % b);
}
